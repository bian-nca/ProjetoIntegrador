    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // salvando os dados do banco 
        
         try {
                String bancos = Descbanco.getText();
                String telefone = Fonebanco.getText();
                String cep = cepCadBanco.getText();
                String endereco = Enderbanco.getText();
                String cidade = CidadeBanco.getText();
                String ufbanco = EstadoBanco.getText();
                String sqlbancos = "INSERT INTO BANCOS(codbanco, descricao, telefone, cep, endereco, cidade, estado) VALUES (NULL, '"+bancos+"','"+telefone+"', '"+cep+"', '"+endereco+"', '"+cidade+"', '"+ufbanco+"')";
                SQLConection conection = new SQLConection();
                conection.SqlExecution(sqlbancos);
                JOptionPane.showMessageDialog(null, "Banco Cadastrado!");
                this.dispose();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Erro na execução do SQL");
            }
        
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void ConsultaCepBancoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConsultaCepBancoActionPerformed
        // TODO add your handling code here:
        Adress adress = new Adress();
        JsonCepConnection jsonCepConnection = new JsonCepConnection();
        jsonCepConnection.setCep(cepCadBanco.getText());
        try {
            adress = jsonCepConnection.buscaEnderecoPelo(cepCadBanco.getText());
        } 
        catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro ao buscar CEP");
            JOptionPane.showMessageDialog(rootPane, "Informe o CEP coreto!");
        }
        Enderbanco.setText(adress.getLogradouro());
        CidadeBanco.setText(adress.getLocalidade());
        EstadoBanco.setText(adress.getUf());
    }//GEN-LAST:event_ConsultaCepBancoActionPerformed

