    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    //buscando minha empresa cadastrada
    //trazendo informações de produto ja cadastrado 
         try {
            String codigo, fantasia_empresa, cnpj_empresa, cep_empresa, rua_empresa, bairro_empresa, numero_empresa, cidade_empresa, estado_empresa, complemento_empresa, ibge_empresa, inscricaoe, regimetributario, phone;
            
            String nome = desc.getText();
            String sql = "SELECT idempresa, descricao, fantasia, CNPJ, CEP, rua, bairro, cidade, numero, estado, complemento, ibge, inscricao, regimetrib, telefone FROM EMPRESA WHERE DESCRICAO LIKE '"+nome+"'";
            Connection con = SQLConection.getConnection();
            PreparedStatement stmt = con.prepareStatement(sql);
            ResultSet rs = stmt.executeQuery(sql);
            
            rs.next();
            
            codigo = Integer.toString(rs.getInt("idempresa"));
            nome = rs.getString("descricao");
            fantasia_empresa = rs.getString("fantasia");
            cnpj_empresa = rs.getString("CNPJ"); /* estava como Integer.getInt(rs.getInt()  e na tabela ao verificar sua propriedade vi que estava como varchar, ocasiando divergência e erros no código...*/
            cep_empresa = rs.getString("cep");
            rua_empresa = rs.getString("rua");
            bairro_empresa = rs.getString("bairro");
            numero_empresa = rs.getString("numero");
            cidade_empresa = rs.getString("cidade");
            estado_empresa = rs.getString("estado");
            complemento_empresa = rs.getString("complemento");
            ibge_empresa = rs.getString("ibge");
            inscricaoe = rs.getString("inscricao");
            regimetributario = rs.getString("regimetrib");
            phone = rs.getString("telefone");
         
            codloja.setText(codigo);
            desc.setText(nome);
            nomefantasia.setText(fantasia_empresa);
            cnpjloja.setText(cnpj_empresa); 
            cep.setText(cep_empresa);
            endereco.setText(rua_empresa);
            bairro.setText(bairro_empresa);
            cidade.setText(cidade_empresa);
            estado.setText(estado_empresa);
            numero.setText(numero_empresa);
            complemento.setText(complemento_empresa);
            ibge.setText(ibge_empresa);
            inscricao.setText(inscricaoe);
            telefone.setText(phone);
            regime.setText(regimetributario);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao consultar empresa!");
            JOptionPane.showMessageDialog(null, "Por favor verificar se essa empresa já está cadastrada!");
        }
       
    }//GEN-LAST:event_jButton1ActionPerformed

